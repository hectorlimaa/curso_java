
public class FuncoesMatematicas {
	public static void main(String[] args) {
		
		double a, x, y;
		
		x = 10;
		y = 20;
		
		a = Math.sqrt(x); // raiz quadrada
		a = Math.pow(x, y); // potenciacao
		a = Math.abs(x); // valor absoluto, tirar sinal negativo caso exista
		
		System.out.println(a);
		
		

	}

}
